    private void jtb_Khach_HangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtb_Khach_HangMouseClicked
        int selectedRow = jtb_Khach_Hang.getSelectedRow();
        if (selectedRow != -1) {  // Kiểm tra xem có hàng nào được chọn
            String id = jtb_Khach_Hang.getValueAt(selectedRow, 0).toString();
            idKhach = Integer.parseInt(id);
        }
    }//GEN-LAST:event_jtb_Khach_HangMouseClicked

    private void jtb_LichBayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtb_LichBayMouseClicked
        int selectedRow = jtb_LichBay.getSelectedRow();
        if (selectedRow != -1) {  // Kiểm tra xem có hàng nào được chọn
            String id = jtb_LichBay.getValueAt(selectedRow, 0).toString();
            idLichBay = Integer.parseInt(id);
            giaVe = Double.parseDouble(jtb_LichBay.getValueAt(selectedRow, 3).toString());
        }
    }//GEN-LAST:event_jtb_LichBayMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (idKhach == 0 || idLichBay == 0) {
            JOptionPane.showMessageDialog(this, "Chưa chọn khách hàng hoặc lịch bay!");
            return;

        } else {
            VeMayBay veMayBay = new VeMayBay();
            veMayBay.setKhachHangID(idKhach);
            veMayBay.setLichBayID(idLichBay);
            veMayBay.setNgayDatVe(java.time.LocalDateTime.now());
            veMayBay.setTrangThai("done");
            veMayBay.setGiaVe(BigDecimal.valueOf(giaVe));
            veMayBayDAO.insertVe(veMayBay);
            JOptionPane.showMessageDialog(this, "Đặt vé thành công!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

