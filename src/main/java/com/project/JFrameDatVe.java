    private void jbtn_Ten_KhachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_Ten_KhachActionPerformed
        String tenKhach = jtf_Ten_Khach.getText();
        if (tenKhach.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên khách hàng.");
            return;
        }
        try {
            List<KhachHang> khachHangList = khachHangDAO.findByName(tenKhach);
            DefaultTableModel model = (DefaultTableModel) jtb_Khach_Hang.getModel();
            model.setRowCount(0);  // Xóa các dòng cũ
            for (KhachHang kh : khachHangList) {
                model.addRow(new Object[]{kh.getKhachHangID(),kh.getHoTen(), kh.getEmail(), kh.getSoDienThoai(), kh.getNgaySinh(), kh.getDiaChi()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tìm kiếm khách hàng: " + e.getMessage());
        }
    }//GEN-LAST:event_jbtn_Ten_KhachActionPerformed

    private void jbtn_Ngay_BayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_Ngay_BayActionPerformed
        String ngayBayStr = jtf_Ngay_Bay.getText();
        if (ngayBayStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng ngày bay.");
            jtf_Ngay_Bay.setText(new Date(System.currentTimeMillis()).toString());

            return;
        }
        try {
            LocalDate ngayBay = LocalDate.parse(ngayBayStr, DateTimeFormatter.ofPattern("yyyy-MM-dd"));
            List<LichBay> lichBayList = lichBayDAO.findByDate(ngayBay);
            DefaultTableModel model = (DefaultTableModel) jtb_LichBay.getModel();
            model.setRowCount(0);  // Xóa các dòng cũ
            for (LichBay lb : lichBayList) {
                model.addRow(new Object[]{lb.getLichBayID(),lb.getNgayGioBay(), lb.getSoGhe(), lb.getGiaVe(), lb.getNoiDen(), lb.getNoiDi()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tìm kiếm lịch bay: " + e.getMessage());
        }
    }//GEN-LAST:event_jbtn_Ngay_BayActionPerformed

