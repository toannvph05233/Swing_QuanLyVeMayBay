    private void jbtn_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_EditActionPerformed
          try {
        // Kiểm tra các trường không được để trống
        if (jtf_Id.getText().isEmpty() || jtf_Noi_Di.getText().isEmpty() || 
            jtf_Noi_Den.getText().isEmpty() || jtf_Thoi_Gian.getText().isEmpty() || 
            jtf_So_Ghe.getText().isEmpty() || jtf_Gia_Ve.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!");
            return;
        }

        // Lấy dữ liệu từ các trường nhập
        int lichBayID = Integer.parseInt(jtf_Id.getText());
        String maChuyenBay = jtf_Id.getText(); // Cần đổi lại tên biến cho đúng
        String noiDi = jtf_Noi_Di.getText();
        String noiDen = jtf_Noi_Den.getText();
        LocalDateTime ngayGioBay = LocalDateTime.parse(jtf_Thoi_Gian.getText());
        int soGhe = Integer.parseInt(jtf_So_Ghe.getText());
        BigDecimal giaVe = new BigDecimal(jtf_Gia_Ve.getText());

        // Kiểm tra ID tồn tại
        if (lichBayDAO.findById(lichBayID) == null) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy lịch bay với ID đã nhập!");
            return;
        }

        // Cập nhật thông tin lịch bay
        LichBay lichBay = new LichBay(lichBayID, maChuyenBay, noiDi, noiDen, ngayGioBay, soGhe, giaVe);
        lichBayDAO.update(lichBay);

        JOptionPane.showMessageDialog(this, "Cập nhật lịch bay thành công!");
        // Refresh lại bảng hoặc cập nhật danh sách nếu cần thiết
                loadLichBayTable();

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Có lỗi xảy ra khi cập nhật lịch bay.");
    }
    }//GEN-LAST:event_jbtn_EditActionPerformed

