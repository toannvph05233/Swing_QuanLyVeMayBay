    private void loadLichBayTable() {
        try {
            List<LichBay> lichBays = lichBayDAO.findAll();
            DefaultTableModel model = (DefaultTableModel) jtb_Lich_Bay.getModel();
            model.setRowCount(0);  // Xóa các dòng cũ
            for (LichBay lb : lichBays) {
                model.addRow(new Object[]{lb.getLichBayID(), lb.getNoiDi(), lb.getNoiDen(), lb.getNgayGioBay(), lb.getGiaVe(), lb.getSoGhe()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải danh sách lich bay: " + e.getMessage());
        }
    }

